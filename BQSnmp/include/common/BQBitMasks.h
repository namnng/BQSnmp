static inline BQUint32 FieldSet(BQUint32 value, BQUint32 mask, BQUint32 shift, BQUint32 fieldValue)
    {
    assert(shift < 32);
    return (value & (~mask)) | ((fieldValue << shift) & mask);
    }

static inline BQUint32 FieldGet(BQUint32 value, BQUint32 mask, BQUint32 shift)
    {
    assert(shift < 32);
    return (value & mask) >> shift;
    }

